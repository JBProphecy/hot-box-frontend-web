////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

import { forwardRef } from "react"
import type { ElementAttributes } from "@/types"
import { Span } from "./vanilla"

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

export const Text = forwardRef<HTMLSpanElement, ElementAttributes>((props, ref) => {
  const { classes = [], ...attributes } = props
  return <Span ref={ref} classes={["text", ...classes]} {...attributes} />
})

